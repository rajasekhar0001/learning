// Different types of streams in cpp
#include <iostream>

using namespace std;

int main() {
    cout << "Hello using cout\n";   // prints data to the console

    cerr << "Hello using cerr\n";   // prints error message to the console

    clog << "Hello using clog\n";   // prints log messages to the console

    int n;

    cin >> n ;  // reads data from the terminal
}